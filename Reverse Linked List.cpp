struct ListNode {
    int val;
    ListNode *next;
    ListNode(int x) : val(x), next(NULL) {}
};

class Solution {
public:
    ListNode* reverseList(ListNode* head) {
    	if (head==NULL) return NULL;
        ListNode* ans=NULL;
        while (head){
			ListNode* next=head->next;
			head->next=ans;
			ans=head;
			head=next;
        }
        return ans;
    }
};

